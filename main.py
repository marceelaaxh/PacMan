def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def distance(a,b):
    """Distancia entre dos puntos con la distancia euclidiana"""
    return ((a.x - b.x) ** 2 + (a.y - b.y) ** 2) ** 0.5

def best_direction(point, pacman, course):
    """ Devuelve la mejor direccion para mover al fantasma """
    options = [
        vector(5, 0),   # Derecha
        vector(-5, 0),  # Izquierda
        vector(0, 5),   # Arriba
        vector(0, -5)   # Abajo
    ]

    # filtrar solo las ociones que son validas
    valid_options = [option for option in options if valid(point + option)]
    
    if not valid_options:
        return course   # cualquier cosa se queda en su estado actual
    
    # aca el codigo elige la dirección que minimiza la distancia entre el fantasma y pacman
    best_option = min(valid_options, key=lambda option: distance(point + option, pacman))
    
    return best_option

def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        # Aca se termina usando la formula euclidiana para mover al fantasma en la mejor dirección
        new_course = best_direction(point, pacman, course)
        point.move(new_course)
        course.x, course.y = new_course.x, new_course.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)


def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


